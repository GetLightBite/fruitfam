class EmojiEscaper(object):
  """Just a bunch of Emojis"""
  def __init__(self):
    pass
  
  def lightning(self):
    return '\xe2\x9a\xa1'
  
  def poo(self):
    return '\xf0\x9f\x92\xa9'
  
  def wink(self):
    return '\xf0\x9f\x98\x89'
  
  def hourglass(self):
    return '\xe2\x8f\xb3'
  
  def point_up(self):
    return '\xe2\x98\x9d\xf0\x9f\x8f\xbe'
  
  def grinning(self):
    return '\xf0\x9f\x98\x81'
  
  def silhouette(self):
    return '\xf0\x9f\x91\xa4'

Emoji = EmojiEscaper()